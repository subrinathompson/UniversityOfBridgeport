package basicmath;

/**
 *
 * @author Subrina
 */
public class BasicMath {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        int a, b;
        
        a = 7;
        b = 5;
        
        System.out.println("Values are:\n\ta = " + a + "\n\tb = " + b);
        System.out.println("\nResults are:\n\tSum: " + (a + b));
        System.out.println("\tProduct: " + (a * b));
        System.out.println("\tDifference: " + (a - b));
        
        
        double c, d;
        
        c = 1.4;
        d = 3.5;
        
        System.out.println("Values are:\n\tc = " + c + "\n\td = " + d);
        System.out.println("\nResults are:\n\tSum: " + (c + d));
        System.out.println("\tProduct: " + (c * d));
        System.out.println("\tDifference: " + (c - d));
    }
    
}
